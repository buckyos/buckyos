export const CONTROL_PANEL_SOURCE_TRANSLATIONS: Record<ControlPanelLocale, SourceTranslationDictionary> = {
  en: {},
  'zh-CN': zhCNSource,
}

export const translate = (
  locale: ControlPanelLocale,
  key: string,
  fallback?: string,
  values?: TranslationValues,
) => {
  const template = CONTROL_PANEL_TRANSLATIONS[locale][key] ?? fallback ?? key
  if (!values) return template

  return Object.entries(values).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template,
  )
}

export const translateSource = (locale: ControlPanelLocale, source: string) => {
  if (locale === 'en') return source

  const trimmed = source.trim()
  const translated = CONTROL_PANEL_SOURCE_TRANSLATIONS[locale][trimmed]
  if (!translated) return source

  const prefixLength = source.indexOf(trimmed)
  const prefix = prefixLength > 0 ? source.slice(0, prefixLength) : ''
  const suffix = prefixLength >= 0 ? source.slice(prefixLength + trimmed.length) : ''
  return `${prefix}${translated}${suffix}`
}
